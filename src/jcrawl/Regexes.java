package jcrawl;

/**
 * This Class has predefined regexes.
 */
public class Regexes {
	
	public static final String GIF = ".+\\.[gG][iI][fF]";
	
	public static final String HTML = ".+\\.[hH][tT][mM][lL]?";
	
	public static final String JPG = ".+\\.[jJ][pP][eE]?[gG]";
	
	public static final String MP3 = ".+\\.[mM][pP]3";
	
	public static final String PDF = ".+\\.[pP][dD][fF]";
	
	public static final String PHP = ".+\\.[pP][hH][pP]";
	
	public static final String PNG = ".+\\.[pP][nN][gG]";
	
	public static final String XML = ".+\\.[xX][mM][lL]";
	
	public static final String ZIP = ".+\\.[zZ][iI][pP]";
	
}
